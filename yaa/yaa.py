# yaa 智能体主程序入口
# 
# 职责：
# 1. 初始化各模块（会话管理、身份验证、智能体等）
# 2. 启动前后端服务
# 3. 处理全局异常和中断
#
# 实现思路：
# - 创建 Session 实例管理用户会话
# - 初始化 Auth 模块处理身份验证
# - 加载默认配置和提示词
# - 启动 API 服务接收前端请求
# - 根据架构图，将请求路由到相应模块处理
#
# 与其他模块关系：
# - 依赖 Session.py 管理会话状态
# - 调用 BaseAgent.py 创建智能体实例
# - 使用 LLM_API.py 提供对外接口
# - 通过 Config/DefaultConfig.py 获取默认配置