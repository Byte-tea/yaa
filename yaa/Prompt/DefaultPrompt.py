# 默认提示词模板模块
#
# 职责：
# 1. 提供各类任务的默认提示词模板
# 2. 支持动态变量插值
# 3. 管理提示词组合逻辑
#
# 主要提示词分类：
# - 任务分析提示词：解析用户意图
# - 工具调用提示词：格式化工具参数
# - 结果总结提示词：整理最终输出
# - 错误处理提示词：异常情况引导
#
# 实现机制：
# 1. 使用类方法组织不同场景的提示词
# 2. 支持上下文变量插值（{variable}语法）
# 3. 提供组合方法合并多个提示词片段
# 4. 支持从配置文件加载自定义模板
#
# 示例模板：
# TASK_ANALYSIS_PROMPT = '''
# 请分析以下任务：{task}
# 预期输出格式：{format}
# '''
#
# 与其他模块关系：
# - 被BaseAgent.py调用来生成完整提示词
# - 从Session.py获取会话上下文
# - 可扩展支持用户自定义模板